# Operator Assignment
# 1. =
# 2. +=
# 3. -=
# 4. *=
# 5. /=
# 6. **=
# 7. %=
# 8. //=

print("Assingment Operator =")
a = int(input("Masukkan Nilai a1: "))
a = int(input("Masukkan Nilai a2: "))
print("Nilai a adalah = ", a)

print("Assingment Operator +=")
b = int(input("Masukkan Nilai b1: "))
b += int(input("Masukkan Nilai b2: "))
print("Nilai b adalah: ", b)

print("Assingment Operator -=")
c = int(input("Masukkan Nilai c1: "))
c -= int(input("Masukkan Nilai c2: "))
print("Nilai c adalah: ", c)

print("Assingment Operator *=")
d = int(input("Masukkan Nilai d1: "))
d *= int(input("Masukkan Nilai d2: "))
print("Nilai d adalah: ", d)

print("Assingment Operator /=")
e = int(input("Masukkan Nilai e1: "))
e /= int(input("Masukkan Nilai e2: "))
print("Nilai e adalah: ", round(e, 3))

print("Assingment Operator **=")
f = int(input("Masukkan Nilai f1: "))
f **= int(input("Masukkan Nilai f2: "))
print("Nilai f adalah: ", f)

print("Assingment Operator %=")
g = int(input("Masukkan Nilai g1: "))
g %= int(input("Masukkan Nilai g2: "))
print("Nilai g adalah: ", g)

print("Assingment Operator //=")
h = int(input("Masukkan Nilai h1: "))
h //= int(input("Masukkan Nilai h2: "))
print("Nilai h adalah: ", h)